    public void load(){
        try {
             
            Connection cn = new MyConnect().getcn();
            PreparedStatement ps = cn.prepareStatement("select * from NguoiDung");
             ResultSet rs = ps.executeQuery();
            list.clear();// xoá list để lấy dữ liệu từ bảng
            while(rs.next()){
                nguoiDung nd = new nguoiDung(rs.getString(1), rs.getString(2), rs.getString(3));
                list.add(nd);
            }
            cn.close();
            ps.close();
            DefaultTableModel model = (DefaultTableModel) tbl_table.getModel();
        model.setRowCount(0);
        for(nguoiDung ndd: list){
            Object[] row = new Object[]{ndd.getTenDangNhap(),ndd.getMatKhau(),ndd.getVaiTro()};
            model.addRow(row);
        }
        } catch (Exception e) {
        }
    }
