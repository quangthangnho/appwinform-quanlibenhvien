       public void show_Detail(){
        // lấy ra sv hiện hành
        nguoiDung nd = list.get(current);
        // gán thông tin sv lên form
        jTextField1.setText(nd.getTenDangNhap());
        jTextField2.setText(nd.getMatKhau());
        jComboBox1.setSelectedItem(nd.getVaiTro());

    }
       public void insert(){
        try {
            nguoiDung nd = new nguoiDung();
        nd.setTenDangNhap(jTextField1.getText());
        nd.setMatKhau(jTextField2.getText());
        nd.setVaiTro(jComboBox1.getSelectedItem().toString());
        nguoiDungDAO dao = new nguoiDungDAO();
            //3. Thêm đối tượng sinh viên stu bằng cách gọi hàm insertStudent trong Student_Grade_Model
            int kq = dao.insertND(nd);
            if (kq == 1) {
                JOptionPane.showMessageDialog(this, "Thêm thành công.");
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại.");
            }

            //4. nhớ gọi lại load_data() để load dữ liệu mới lên jtable
            load();
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Thêm Không Thành Công.");
            e.printStackTrace();
        }
    }
       public void update(){
        try {
            nguoiDung nd = new nguoiDung();
        nd.setTenDangNhap(jTextField1.getText());
        nd.setMatKhau(jTextField2.getText());
        nd.setVaiTro(jComboBox1.getSelectedItem().toString());
        nguoiDungDAO dao = new nguoiDungDAO();
            int kq = dao.updateND(nd);
            if (kq == 1) {
                JOptionPane.showMessageDialog(this, "Sửa thành công.");
            } else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại.");
            }
            load();
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Sửa Không Thành Công.");
            e.printStackTrace();
        }
    }
       public void delete() {
        try {
            nguoiDungDAO dao = new nguoiDungDAO();
            int kq = dao.deleteND(jTextField1.getText());
            if (kq == 1) {
                JOptionPane.showMessageDialog(this, "Xóa thành công.");
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại.");
            }
            load();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Xóa Không Thành Công.");
            e.printStackTrace();
        }
    }

